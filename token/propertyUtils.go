func IsCssProperty(value string) (subkind Subkind, ok bool) {
	if subkind, ok = IsCssStandardProperty(value); ok {
		return
	}
	if subkind, ok = IsCssMozProperty(value); ok {
		return
	}
	if subkind, ok = IsCssWebkitProperty(value); ok {
		return
	}
	return SUBKIND_NONE, false
}

